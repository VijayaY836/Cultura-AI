export const getRelatedEntities = (entityId) => {
  const entity = culturalData.find(e => e.id === entityId);
  if (!entity) return [];
  
  return culturalData.filter(e => 
    entity.connections.includes(e.id) || 
    e.connections.includes(entityId) ||
    e.region === entity.region ||
    e.season === entity.season
  ).slice(0, 5);
};

export const searchCulturalData = (query) => {
  const lowerQuery = query.toLowerCase();
  return culturalData.filter(entity => 
    entity.name.toLowerCase().includes(lowerQuery) ||
    entity.description.toLowerCase().includes(lowerQuery) ||
    entity.region.toLowerCase().includes(lowerQuery) ||
    entity.type.toLowerCase().includes(lowerQuery) ||
    entity.rituals.some(r => r.toLowerCase().includes(lowerQuery)) ||
    entity.communities.some(c => c.toLowerCase().includes(lowerQuery))
  );
};